type I18nContextValue = {
  language: Lang
  setLanguage: (lang: Lang) => void
  t: (key: string) => string
}

const I18nContext = createContext<I18nContextValue | null>(null)

const storageKey = 'xhomes.lang'

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguage] = useState<Lang>(() => {
    const saved = (typeof window !== 'undefined' ? window.localStorage.getItem(storageKey) : null) as Lang | null
    return saved === 'zh' ? 'zh' : 'en'
  })

  useEffect(() => {
    window.localStorage.setItem(storageKey, language)
    document.documentElement.lang = language
  }, [language])

  const value = useMemo<I18nContextValue>(() => {
    const t = (key: string) => messages[language][key] ?? messages.en[key] ?? key
    return { language, setLanguage, t }
  }, [language])

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>
}

export function useI18n() {
  const ctx = useContext(I18nContext)
  if (!ctx) {
    const fallbackT = (key: string) => key
    return { language: 'en' as Lang, setLanguage: () => undefined, t: fallbackT }
  }
  return ctx
}
